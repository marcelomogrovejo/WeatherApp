import Foundation

enum DegreesType: String {
    case celsius = "°C"
    case fahrenheit = "°F"
}

enum WeatherCondition: String {
    case sunny = "Sunny"
    case cloudy = "Cloudy"
    case rainy = "Rainy"
    case windy = "Windy"
    case snowy = "Snowy"
    case foggy = "Foggy"
    case stormy = "Stormy"
    case hot = "Hot"
    case cold = "Cold"
    case humid = "Humid"
}
